    <p>Kebijakan Privasi ini menjelaskan bagaimana data Anda dikumpulkan, digunakan, dan dilindungi ketika Anda menggunakan aplikasi <strong>YMSoft ERP</strong> (“Aplikasi”). Dengan menggunakan Aplikasi, Anda menyetujui ketentuan dalam kebijakan ini.</p>

    <h2>1. Data yang Kami Kumpulkan</h2>
    <p>Aplikasi dapat mengumpulkan dan memproses data berikut:</p>
    <ul>
        <li><strong>Data akun:</strong> nama pengguna, alamat email, dan informasi profil yang Anda berikan saat login atau mengelola akun.</li>
        <li><strong>Data perangkat:</strong> informasi perangkat (misalnya model, sistem operasi) dan pengenal perangkat untuk keperluan autentikasi dan notifikasi push.</li>
        <li><strong>Data penggunaan:</strong> aktivitas dalam Aplikasi (misalnya approval, pengajuan, riwayat) yang diperlukan untuk menyediakan layanan ERP dan pelaporan.</li>
    </ul>

    <h2>2. Penggunaan Data</h2>
    <p>Data tersebut digunakan untuk:</p>
    <ul>
        <li>Menyediakan dan mengoperasikan layanan Aplikasi (approval, reservasi, pengajuan, dan fitur ERP lainnya).</li>
        <li>Mengirim notifikasi push terkait tugas, persetujuan, atau pengumuman yang relevan dengan peran Anda.</li>
        <li>Keamanan akun, autentikasi, dan pencegahan penyalahgunaan.</li>
        <li>Pemenuhan kewajiban hukum atau kebijakan perusahaan, sejauh diwajibkan.</li>
    </ul>

    <h2>3. Penyimpanan dan Keamanan</h2>
    <p>Data disimpan di server yang dikelola oleh penyedia layanan kami dan dilindungi dengan langkah-langkah teknis dan organisasi yang wajar. Komunikasi antara Aplikasi dan server menggunakan protokol aman (HTTPS).</p>

    <h2>4. Berbagi Data</h2>
    <p>Data tidak dijual ke pihak ketiga. Data dapat dibagikan hanya dalam rangka penyediaan layanan (misalnya infrastruktur cloud atau notifikasi) atau jika diwajibkan oleh hukum.</p>

    <h2>5. Hak Anda</h2>
    <p>Anda dapat meminta akses, koreksi, atau penghapusan data pribadi Anda melalui kontak administrator sistem atau tim IT perusahaan Anda. Beberapa data mungkin wajib disimpan untuk keperluan hukum atau operasional.</p>

    <h2>6. Perubahan Kebijakan</h2>
    <p>Kebijakan Privasi ini dapat diperbarui dari waktu ke waktu. Perubahan material akan diberitahukan melalui Aplikasi atau saluran resmi perusahaan. Penggunaan Aplikasi setelah perubahan dianggap sebagai penerimaan kebijakan yang diperbarui.</p>

    <h2>7. Kontak</h2>
    <p>Untuk pertanyaan tentang Kebijakan Privasi atau data pribadi Anda, hubungi tim IT atau administrator sistem di perusahaan Anda.</p>

